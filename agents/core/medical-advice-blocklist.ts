/**
 * 제목/본문에서 의료 조언/판단 요청을 감지한다.
 * @returns 매칭된 { pattern, field } 또는 null (경험담·일상은 null)
 */
export function findMedicalAdviceRequest(
  title: string,
  content: string,
): { pattern: string; field: 'title' | 'content' } | null {
  const t = title ?? ''
  const c = content ?? ''
  for (const re of MEDICAL_ADVICE_PATTERNS) {
    const mt = t.match(re)
    if (mt) return { pattern: mt[0], field: 'title' }
    const mc = c.match(re)
    if (mc) return { pattern: mc[0], field: 'content' }
  }
  return null
}

export function hasMedicalAdviceRequest(title: string, content: string): boolean {
  return findMedicalAdviceRequest(title, content) !== null
}
